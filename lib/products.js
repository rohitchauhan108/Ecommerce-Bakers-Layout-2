const categoryOrder = {
  "biscuit-and-confections": 1,
  "rusk": 2,
  "tea-time-cake": 3,
  "fresh-bread": 4,
};

function defaultDescForCategory(cat) {
  if (cat === "tea-time-cake") return "Celebration & everyday cakes";
  if (cat === "biscuit-and-confections") return "Crispy & buttery delights";
  if (cat === "rusk") return "Traditional baked rusks";
  if (cat === "fresh-bread") return "Breads baked fresh";
  return "";
}

export const orderedProducts = products
  .slice()
  .sort((a, b) => {
    const c =
      (categoryOrder[a.category] ?? 9) - (categoryOrder[b.category] ?? 9);
    if (c !== 0) return c;
    return a.name.localeCompare(b.name);
  })
  .map((p, idx) => ({
    ...p,
    id: idx + 1,
    description: p.description ?? defaultDescForCategory(p.category),
  }));

